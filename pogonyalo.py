def nickname_core(n: str) -> str:
    s = n
    if s.lower().startswith("–ª–µ–ø–º–∞–Ω"):
        s = s[len("–õ–µ–ø–º–∞–Ω"):].strip(" ‚Äî-")
    return s.strip()

def pick_for_history(history: List[str]) -> str:
    pool = [n for n in NICKNAMES if n not in history]
    if not pool:
        history.clear()
        pool = NICKNAMES[:]
    choice = random.choice(pool)
    history.append(choice)
    if len(history) > 7:
        history.pop(0)
    return choice

def pick_name_html(chat_id: int) -> str:
    state = load_state()
    entry = _ensure_entry(chat_id)
    nick = pick_for_history(entry["history"])
    state[str(chat_id)] = entry
    save_state(state)
    core = nickname_core(nick)
    return f"<b>–õ–µ–ø–º–∞–Ω</b> ‚Äî <i>{html.escape(core)}</i>"

def pick_poll_options(entry: Dict[str, Any], k: int = 5) -> List[str]:
    opts = set()
    attempts = 0
    while len(opts) < k and attempts < 1000:
        attempts += 1
        n = pick_for_history(entry["history"])
        opts.add(nickname_core(n))
    res = list(opts)
    while len(res) < k:
        res.append(res[-1] if res else "–õ–µ–ø–º–∞–Ω")
    return res[:k]


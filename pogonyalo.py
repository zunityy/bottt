async def create_daily_poll(chat_id: int, context: ContextTypes.DEFAULT_TYPE) -> None:
    state = load_state()
    entry = _ensure_entry(chat_id)
    if not entry.get("subscribed"):
        return

    options = pick_poll_options(entry, k=5)
    question = "–õ–µ–ø–º–∞–Ω —Å–µ–≥–æ–¥–Ω—è:\n–∏—Ç–æ–≥–∏ —á–µ—Ä–µ–∑ 2 —á–∞—Å–∞!"

    msg = await context.bot.send_poll(
        chat_id=chat_id,
        question=question,
        options=options,
        is_anonymous=False,
        allows_multiple_answers=False,
    )

    # –ó–∞–∫—Ä—ã—Ç—å –æ–ø—Ä–æ—Å –∏ –æ–±—ä—è–≤–∏—Ç—å –ø–æ–±–µ–¥–∏—Ç–µ–ª—è —á–µ—Ä–µ–∑ 2 —á–∞—Å–∞
    context.job_queue.run_once(
        close_poll_and_announce,
        when=timedelta(hours=2),
        chat_id=chat_id,
        data={"message_id": msg.message_id, "options": options},
        name=f"closepoll:{chat_id}:{msg.message_id}",
    )

async def close_poll_and_announce(ctx: ContextTypes.DEFAULT_TYPE) -> None:
    chat_id = ctx.job.chat_id
    data = ctx.job.data or {}
    message_id = data.get("message_id")
    options = data.get("options", [])

    if not message_id:
        return

    try:
        poll = await ctx.bot.stop_poll(chat_id=chat_id, message_id=message_id)
    except Exception as e:
        print(f"stop_poll failed for chat {chat_id}, msg {message_id}: {e}")
        return

    max_votes = -1
    leaders: List[str] = []
    for opt in poll.options:
        if opt.voter_count > max_votes:
            max_votes = opt.voter_count
            leaders = [opt.text]
        elif opt.voter_count == max_votes:
            leaders.append(opt.text)
    if not leaders:
        leaders = options or ["–õ–µ–ø–º–∞–Ω"]

    winner = random.choice(leaders)

    sent = await ctx.bot.send_message(
        chat_id=chat_id,
        text=f"–°–µ–≥–æ–¥–Ω—è –õ–µ–ø–º–∞–Ω ‚Äî <b>{html.escape(winner)}</b>!",
        parse_mode="HTML",
    )

    # –ü–æ–ø—ã—Ç–∫–∞ –ø–æ—Å—Ç–∞–≤–∏—Ç—å —Ä–µ–∞–∫—Ü–∏—é (–Ω–µ –∫—Ä–∏—Ç–∏—á–Ω–æ)
    try:
        from telegram._types import ReactionTypeEmoji
        await ctx.bot.set_message_reaction(
            chat_id=chat_id,
            message_id=sent.message_id,
            reaction=[ReactionTypeEmoji(emoji=random.choice(['üëç', 'üëé']))],
            is_big=False,
        )
    except Exception:
        pass


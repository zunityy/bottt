app = FastAPI()

@app.on_event("startup")
async def startup():
    # PTB application start
    await application.initialize()
    await application.start()
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º webhook –≤ Telegram
    webhook = WEBHOOK_URL
    if not webhook:
        # –ø—Ä–æ–±—É–µ–º —Å–æ–±—Ä–∞—Ç—å –∏–∑ Render –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –æ–∫—Ä—É–∂–µ–Ω–∏—è
        host = os.environ.get("RENDER_EXTERNAL_HOSTNAME")
        if host:
            webhook = f"https://{host}/{TOKEN}"
    if webhook:
        try:
            await application.bot.set_webhook(webhook)
            print("Webhook set to:", webhook)
        except Exception as e:
            print("Failed to set webhook:", e)
    else:
        print("No WEBHOOK_URL provided and RENDER_EXTERNAL_HOSTNAME not found; please set WEBHOOK_URL env var")

@app.on_event("shutdown")
async def shutdown():
    try:
        await application.stop()
        await application.shutdown()
    except Exception:
        pass

@app.post("/{token}")
async def telegram_webhook(token: str, request: Request):
    """Endpoint –¥–ª—è Telegram webhook: POST /<TOKEN>"""
    if token != TOKEN:
        return {"ok": False, "error": "invalid token in path"}

    data = await request.json()
    update = Update.de_json(data, application.bot)
    # –ø–æ–º–µ—â–∞–µ–º update –≤ –æ—á–µ—Ä–µ–¥—å –æ–±—Ä–∞–±–æ—Ç–∫–∏ PTB
    await application.update_queue.put(update)
    return {"ok": True}

